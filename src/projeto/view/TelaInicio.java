    public void LoadTable() {

        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Codigo", "Nome", "Categotia"}, 0);

        for (int i = 0; i < daoProd.getTotal(); i++) {
            Object linha[] = new Object[]{daoProd.listaGeral().get(i).getCodigo(),
                daoProd.listaGeral().get(i).getNome(), daoProd.listaGeral().get(i).getCat()};               
            modelo.addRow(linha);
        }

        tbl_Prod.setModel(modelo);
        tbl_Prod.getColumnModel().getColumn(0).setPreferredWidth(50);
        tbl_Prod.getColumnModel().getColumn(1).setPreferredWidth(200);
        tbl_Prod.getColumnModel().getColumn(2).setPreferredWidth(200);

    }

