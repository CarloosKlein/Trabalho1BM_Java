    private void btn_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarActionPerformed
        // TODO add your handling code here:
        String idStr = txt_pesquisa.getText();

        if (totalProd() >= 1) {
            if (txt_pesquisa.getText().length() > 0) {
                Produto func = new Produto();
                try {
                    func = daoProd.pequisaProduto(idStr, daoProd.listaGeral());
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Aconteceu um erro:" + e);
                }
                if (func != null) {
                                       
                    txt_codigo.setText(String.valueOf(func.getCodigo()));
                    txt_Prod.setText(func.getNome());
                    modoProd = "Novo";
                    ManipulaInterfaceHome();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Você não tem nenhum Funcionario cadastrado!");
        }
    }//GEN-LAST:event_btn_pesquisarActionPerformed

    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        int cod = totalProd() + 1;
        if (modoProd.equals("Novo")) {
            Produto P = new Produto(cod, txt_Prod.getText(),txt_Cat.getText());
            daoProd.addProduto(P);
        } else if (modoProd.equals("Editar")) {
            int index = tbl_Prod.getSelectedRow();
            int codedit = Integer.parseInt(txt_codigo.getText());
            daoProd.editarProduto(index).setCodigo(codedit);
            daoProd.editarProduto(index).setNome(txt_Prod.getText());
        }

        LoadTable();
        modoProd = "Navegar";
        ManipulaInterfaceHome();
        txt_codigo.setText(Integer.toString(cod + 1));
    }//GEN-LAST:event_btn_SalvarActionPerformed

