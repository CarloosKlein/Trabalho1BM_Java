    private void btn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExcluirActionPerformed
        // TODO add your handling code here:
        int index = tbl_Prod.getSelectedRow(),
                i = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Excluir?", "Excluir", JOptionPane.YES_NO_OPTION);

        if (i == JOptionPane.YES_OPTION) {
            if (index >= 0 && index < daoProd.getTotal()) {
                daoProd.removeProduto(index);
            }
        } else {
            repaint();
        }

        LoadTable();
        modoProd = "Navegar";
        ManipulaInterfaceHome();
    }//GEN-LAST:event_btn_ExcluirActionPerformed

